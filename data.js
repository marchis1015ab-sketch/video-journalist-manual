function createEmptyStatLine(keys) {
  const entry = {};
  keys.forEach((key) => {
    entry[key] = 0;
  });
  return entry;
}

function createEmptyBatterStats() {
  return createEmptyStatLine(BATTER_STAT_KEYS);
}

function createEmptyPitcherStats() {
  return createEmptyStatLine(PITCHER_STAT_KEYS);
}

function createEmptyTeamState() {
  return {
    games: 0,
    wins: 0,
    losses: 0,
    draws: 0,
    runsScored: 0,
    runsAllowed: 0,
    results: [],
  };
}

function buildPlayerMap(factory) {
  const map = {};
  PLAYERS.forEach((player) => {
    map[player] = factory();
  });
  return map;
}

function normalizeHistoricalRowsByYear(source, keys) {
  const result = {};
  Object.entries(source).forEach(([year, rows]) => {
    result[year] = rows.map((row) => ({
      ...row,
      ...cloneStatLine(row, keys),
    }));
  });
  return result;
}

function normalizeCareerMap(source, keys, factory) {
  const map = buildPlayerMap(factory);
  Object.entries(source).forEach(([player, stats]) => {
    map[player] = cloneStatLine(stats, keys);
  });
  return map;
}

function loadStoredPlayerMap(storageKey, keys, factory) {
  const base = buildPlayerMap(factory);
  try {
    const raw = localStorage.getItem(storageKey);
    if (!raw) {
      return base;
    }
    const parsed = JSON.parse(raw);
    PLAYERS.forEach((player) => {
      keys.forEach((key) => {
        base[player][key] = Number(parsed?.[player]?.[key] ?? 0) || 0;
      });
    });
  } catch (error) {
    console.warn('[records] failed to load player map', storageKey, error);
  }
  return base;
}

function loadStoredTeamState(storageKey) {
  const base = createEmptyTeamState();
  try {
    const raw = localStorage.getItem(storageKey);
    if (!raw) {
      return base;
    }
    const parsed = JSON.parse(raw);
    base.games = Number(parsed?.games ?? 0) || 0;
    base.wins = Number(parsed?.wins ?? 0) || 0;
    base.losses = Number(parsed?.losses ?? 0) || 0;
    base.draws = Number(parsed?.draws ?? 0) || 0;
    base.runsScored = Number(parsed?.runsScored ?? 0) || 0;
    base.runsAllowed = Number(parsed?.runsAllowed ?? 0) || 0;
    base.results = Array.isArray(parsed?.results) ? parsed.results : [];
  } catch (error) {
    console.warn('[records] failed to load team state', error);
  }
  return base;
}

